    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed
        // TODO add your handling code here:
        String nome = tf_nome.getText();
        String str_nPoltronas = tf_nPoltronas.getText();
   
        
        int mensagem = LanchaController.cadastrarLancha(nome, str_nPoltronas);
        switch(mensagem){
            case 1 -> {
                JOptionPane.showMessageDialog(rootPane, String.format("Lancha %s cadastrada com sucesso!", nome), "Sucesso", JOptionPane.INFORMATION_MESSAGE, IconeUtil.getIconeSucesso());
                this.dispose();
            }
            case 2 -> {
                JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos.", "Erro: campos incompletos", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());
            }
            case 3 -> {
                tf_nPoltronas.setText("");
                JOptionPane.showMessageDialog(rootPane, "NÃºmero de poltronas invÃ¡lido.", "Erro", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());            
            }
            case 4 -> {
                tf_nome.setText("");
                JOptionPane.showMessageDialog(rootPane, "Nome de embarcaÃ§Ã£o jÃ¡ utilizado.", "Erro", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());            
            }
            case 5 -> {
                tf_nome.setText("");
                tf_nPoltronas.setText("");
                JOptionPane.showMessageDialog(rootPane, "Erro inesperado aconteceu.", "Erro", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());            
            }
        }
        
    }//GEN-LAST:event_btn_cadastrarActionPerformed

