    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed
        String nomeCompleto = tf_nomeCompleto.getText();
        String nomeUsuario = tf_nomeUsuario.getText();
        String senha = new String(tf_senha.getPassword());
        String confirmacaoSenha = new String(tf_confirmacaoSenha.getPassword());
        
        tf_senha.setText("");
        tf_confirmacaoSenha.setText("");
                
        int mensagem = GerenteController.cadastrarGerente(nomeCompleto, nomeUsuario, senha, confirmacaoSenha);
        switch(mensagem){
            case 1 -> {
                JOptionPane.showMessageDialog(rootPane, String.format("Gerente cadastrado: %s!", nomeUsuario), "Sucesso!", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeSucesso());
                this.dispose();
                new TelaLogin().setVisible(true);
            }
            case 2 -> {
                JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos.", "Erro: campos incompletos", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());
            }
            case 3 -> {
                JOptionPane.showMessageDialog(rootPane, "Senha deve possuir no m√≠nimo 6 caracteres.", "Erro ao cadastrar", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());
            }
            case 4 -> {
                JOptionPane.showMessageDialog(rootPane, "Senhas inseridas devem ser iguais.", "Erro ao cadastrar", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());
            }
        }
    }//GEN-LAST:event_btn_cadastrarActionPerformed

