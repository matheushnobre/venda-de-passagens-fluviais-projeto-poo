    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        // TODO add your handling code here:
        int linha = jt_tabelaVendedores.getSelectedRow();
        
        if(linha >= 0){
            String usuario = jt_tabelaVendedores.getValueAt(linha, 1).toString();
            int confirmacao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja deletar o vendedor " + usuario + "?", "Confirmação", JOptionPane.YES_NO_OPTION);
            if (confirmacao == JOptionPane.YES_OPTION) {
                if(VendedorController.deletarVendedor(usuario)){
                    carregarDados(); // Atualiza a tabela
                    JOptionPane.showMessageDialog(rootPane, "Deletado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE, IconeUtil.getIconeSucesso());
                } else{
                    JOptionPane.showMessageDialog(rootPane, "Um erro inesperado aconteceu.", "Erro", JOptionPane.INFORMATION_MESSAGE, IconeUtil.getIconeErro());
                }
            }
        } 
        else{
            // não foi selecionado vendedor para ser deletado
            JOptionPane.showMessageDialog(rootPane, "Selecione um vendedor", "Erro: nenhum vendedor selecionado", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void jb_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_editarActionPerformed
        // TODO add your handling code here:
        int linha = jt_tabelaVendedores.getSelectedRow();
        if(linha >= 0){
            String usuario = jt_tabelaVendedores.getValueAt(linha, 1).toString();
            String str_salarioAntigo = jt_tabelaVendedores.getValueAt(linha, 2).toString().replace(",", ".");            
            String str_novoSalario = JOptionPane.showInputDialog("Insira o novo salário de " + usuario);
           
            if(str_novoSalario == null) return; // usuário desistiu de editar o salário
            str_novoSalario = str_novoSalario.replace(",", "."); // usuário pode utilizar ',' ou '.' para representar parte decimal
            
            int mensagem = VendedorController.alterarSalario(usuario, str_salarioAntigo, str_novoSalario);
            switch(mensagem){
                case 1 -> {
                    JOptionPane.showMessageDialog(rootPane, "Salário alterado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE, IconeUtil.getIconeSucesso());
                    double novoSalario = Double.parseDouble(str_novoSalario);
                    jt_tabelaVendedores.setValueAt(String.format("%.2f", novoSalario), linha, 2);
                }
                case 2 -> {
                    JOptionPane.showMessageDialog(rootPane, "Salário deve ser um valor númerico.", "Erro: salário inválido", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());
                }
                case 3 -> {
                    JOptionPane.showMessageDialog(rootPane, "Salário deve ser superior ao salário antigo.", "Erro: salário inferior ao anterior", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro()); 
                }
                case 4 -> {
                    JOptionPane.showMessageDialog(rootPane, "Um erro inesperado aconteceu..", "Erro", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro()); 
                }
            }
        } else{
            // nenhum vendedor selecionado
            JOptionPane.showMessageDialog(rootPane, "Selecione um vendedor", "Erro: nenhum vendedor selecionado", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());            
        }
    }//GEN-LAST:event_jb_editarActionPerformed

