    private void carregarDados(){
        ArrayList<Vendedor> vendedores = VendedorDAO.carregarVendedores();
        
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Nenhuma célula será editável
            }
        };
        modelo.addColumn("Nome Completo");
        modelo.addColumn("Usuário");
        modelo.addColumn("Salário");

        for (Vendedor v : vendedores) {
            modelo.addRow(new Object[]{
                v.getNomeCompleto(),
                v.getNomeUsuario(),
                String.format("%.2f", v.getSalario())
            });
        }

        jt_tabelaVendedores.setModel(modelo);
        
    }

