    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        // TODO add your handling code here:
        int linha = jt_tabelaBarcos.getSelectedRow();
        if(linha >= 0){
            String nomeBarco = jt_tabelaBarcos.getValueAt(linha, 0).toString();
            int nRedes = Integer.parseInt(jt_tabelaBarcos.getValueAt(linha, 1).toString());
            int nCamarotes = Integer.parseInt(jt_tabelaBarcos.getValueAt(linha, 2).toString());
            int nAndares = Integer.parseInt(jt_tabelaBarcos.getValueAt(linha, 3).toString());
             
            Barco barco = new Barco(nomeBarco, nRedes, nCamarotes, nAndares);
            TelaEdicaoBarco telaEdicao = new TelaEdicaoBarco(barco);
            telaEdicao.setVisible(true);
            telaEdicao.addWindowListener(new java.awt.event.WindowAdapter() {
            
            @Override
            public void windowClosed(java.awt.event.WindowEvent e) {
                carregarDados(); // Só atualiza a tabela depois que a janela de edição for fechada
            }
            });
            
            return;
        }
        
        JOptionPane.showMessageDialog(rootPane, "Selecione um barco", "Erro: nenhum barco selecionado", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());
    }//GEN-LAST:event_btn_editarActionPerformed

    private void btn_deletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deletarActionPerformed
        // TODO add your handling code here:
        int linha = jt_tabelaBarcos.getSelectedRow();
        if(linha >= 0){
            String nomeBarco = jt_tabelaBarcos.getValueAt(linha, 0).toString();
            int confirmacao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja deletar o barco " + nomeBarco + "?", "Confirmação", JOptionPane.YES_NO_OPTION);
            
            if (confirmacao == JOptionPane.YES_OPTION) {
                if(BarcoController.deletar(nomeBarco)){ 
                    carregarDados(); // 
                    JOptionPane.showMessageDialog(rootPane, "Deletado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE, IconeUtil.getIconeSucesso());
                    return;
                } 
                
                JOptionPane.showMessageDialog(rootPane, "Um erro inesperado aconteceu.", "Erro", JOptionPane.INFORMATION_MESSAGE, IconeUtil.getIconeErro());
            }
            return;
        }
            
        JOptionPane.showMessageDialog(rootPane, "Selecione um barco", "Erro: nenhum barco selecionado", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());
        
    }//GEN-LAST:event_btn_deletarActionPerformed

