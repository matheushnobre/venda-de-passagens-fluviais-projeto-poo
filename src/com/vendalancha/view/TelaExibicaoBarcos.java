    private void estilizarHeader(){
        JTableHeader header = jt_tabelaBarcos.getTableHeader();
        header.setFont(new Font("Comic Sans MS", Font.BOLD, 15));
        header.setBackground(new Color(173,216,230));
        header.setForeground(Color.BLACK);
    }
    
    private void carregarDados(){
        ArrayList<Barco> embarcacoes = BarcoDAO.carregarEmbarcacoes();
        
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Nenhuma célula será editável
            }
        };
        modelo.addColumn("Nome da Embarcação");
        modelo.addColumn("Nº Redes");
        modelo.addColumn("Nº Camarotes");
        modelo.addColumn("Nº Andares");

        for (Barco e : embarcacoes) {
            modelo.addRow(new Object[]{
                e.getNome(),
                e.getnRedes(),
                e.getnCamarotes(),
                e.getnAndares()
            });
        }
        jt_tabelaBarcos.setModel(modelo);
        
    }

