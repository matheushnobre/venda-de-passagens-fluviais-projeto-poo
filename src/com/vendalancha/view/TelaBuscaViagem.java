    private void carregarTabela(ArrayList<RotaViagem> rotasDisponiveis){
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Nenhuma célula será editável
            }
        };
        
        modelo.addColumn("ID");
        modelo.addColumn("Embarcação");
        modelo.addColumn("Horário");
        modelo.addColumn("Vagas IND");
        modelo.addColumn("Preço IND");
        modelo.addColumn("Vagas COL");
        modelo.addColumn("Preço COL");

        for (RotaViagem r : rotasDisponiveis) {
            modelo.addRow(new Object[]{
                r.getId(),
                r.getEmbarcacao().getNome(),
                ConversorData.strDateTimeParaStrHora(r.getHorarioPartida()),
                "x",
                String.format("%.2f", r.getPrecoAcomodacaoIndividual()),
                "x",
                String.format("%.2f", r.getPrecoAcomodacaoColetiva())
            });
        }
        jt_tabelaViagens.setModel(modelo);
    }
