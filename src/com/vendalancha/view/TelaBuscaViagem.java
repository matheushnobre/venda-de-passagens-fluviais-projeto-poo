    private void btn_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesquisarActionPerformed
        // TODO add your handling code here:
        String origem = tf_origem.getText();
        String destino = tf_destino.getText();
        String str_dataPesquisa = tf_data.getText();
        
        int mensagem = RotaViagemController.isPossivelCarregarRotas(origem, destino, str_dataPesquisa);
        switch(mensagem){
            case 1 -> {
               ArrayList<RotaViagem> rotasDisponiveis = RotaViagemController.carregarRotas(origem, destino, str_dataPesquisa); 
               if(rotasDisponiveis == null){
                    JOptionPane.showMessageDialog(rootPane, "Um erro inesperado aconteceu.", "Erro", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());
                    tf_data.setText("");         
               }
               else if(rotasDisponiveis.isEmpty()){
                    JOptionPane.showMessageDialog(rootPane, "Nenhuma rota encontrada.", "Erro: sem rota disponível", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());
                    tf_data.setText("");
               }else
                   carregarTabela(rotasDisponiveis);
            }
            case 2 -> {
                JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos.", "Erro: campos incompletos", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());
            }
            case 3 -> {
                tf_data.setText("");
                JOptionPane.showMessageDialog(rootPane, "Data deve estar no formato (dd/mm/aaaa) e deve ser uma data válida.", "Erro: data inválida", JOptionPane.ERROR_MESSAGE, IconeUtil.getIconeErro());
            }
        }
        
    }//GEN-LAST:event_btn_pesquisarActionPerformed

